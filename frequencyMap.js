function noteName(value) {
	let lower = -1
	let upper = -1
	for (let i=0; i<frequencyArray.length; i++) {
		if (value < frequencyArray[i]) {
			upper = frequencyArray[i]
			lower = frequencyArray[i-1]
			break
		}
	}
	if (lower == -1 || upper == -1) {
		return 'No single pitch identified'
	}
	// find closest now
	let Ldiff = value - lower
	let Udiff = upper - value
	if (Udiff < Ldiff) { //closer to upper value
		return noteFrequencyMap[upper]
	} else { // closer to lower value
		return noteFrequencyMap[lower]
	}
}
